def dist_between(x1, x2):
        # print(x1)
        # print(x2)
        # print(x1[0], x2[0], x1[1], x2[1])

        distance = np.sqrt((x1[0] - x2[0])**2 + (x1[1] - x2[1])**2)
        return distance

def get_path_length(path):
	total_length = 0
	for pi in range(1, len(path)):
		p0 = path[pi - 1]
		p1 = path[pi]

		total_length += dist_between(p0, p1)
	return total_length


